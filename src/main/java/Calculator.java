public class Calculator {
    public double adding (double a, double b) {
        return a + b;
    }

    public double substract (double a, double b) {
        return a - b;
    }

    public double muptiply (double a, double b) {
        return a * b;
    }

    public double divide (double a, double b) {
        return a / b;
    }
}
